/**
 * Definition for a binary tree temp.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func deleteNode(root *TreeNode, key int) *TreeNode {
	if root == nil {
		return nil
	}
	// 用一个节点保存父节点
	var cur, curParent *TreeNode = root, nil
	for cur != nil && cur.Val != key {
		curParent = cur
		if cur.Val > key {
			cur = cur.Left
		} else {
			cur = cur.Right
		}
	}
	// 没找到直接返回
	if cur == nil {
		return root
	}
	// 找到了
	// 如果在叶子结点直接删除
	// 如果左子树或右子树为空，直接子树替换
	// 都不是的话，就把左子树替换上去，右子树加到左子树的最下面右子节点上
	var temp *TreeNode
	switch {
	case cur.Left == nil && cur.Right == nil:
		cur = nil
	case cur.Right == nil:
		cur = cur.Left
	case cur.Left == nil:
		cur = cur.Right
	default:
		temp = cur.Left
		for temp.Right != nil {
			temp = temp.Right
		}
		temp.Right = cur.Right
		cur = cur.Left
	}
	if curParent == nil {
		return cur
	}
	if curParent.Left != nil && curParent.Left.Val == key {
		curParent.Left = cur
	} else {
		curParent.Right = cur
	}
	return root
}

